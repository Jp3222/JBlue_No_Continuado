    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user, pass, url;
        url = jtfURL.getText();
        user = jtfUsuario.getText();
        pass = jtfContra.getText();
        Conexion cn = Conexion.getInstancia(user, pass, url);
        if (cn.isConexion()) {
            JOptionPane.showMessageDialog(null, "Conexion exitosa");
        } else {
            JOptionPane.showMessageDialog(null, "Conexion erronea");
            cn.desconectar();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        instancia.run();
        String user, pass, url;
        url = jtfURL.getText();
        user = jtfUsuario.getText();
        pass = jtfContra.getText();
        //
        AText o = new AText();
        String txt = user + "," + pass + "," + url;
        File file = instancia.getFiles(1);
        o.Escribir_Archivo(file, txt);
        JOptionPane.showMessageDialog(null, "Datos Guardados");
        dispose();
        JOptionPane.showMessageDialog(null, "Por favor vuelva a ejecutar el programa");
    }//GEN-LAST:event_jButton2ActionPerformed


